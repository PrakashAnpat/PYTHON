# Numpy bulit in match functions
from numpy import *
a=array([25,81,49,64])
b=([4,9,16,25],[36,16,4,49])
print(a)
print(b)
print("sum() function")# Returns sum all the elements in the array.
print("a:",sum(a))
print("b:",sum(b))
print("prod() function")# Returns product of all the elements in the array.
print("a:",prod(a))
print("b:",prod(b))
print("sqrt() function")# Returns square root value of each element in the array.
print("a:",sqrt(a))
print("b:",sqrt(b))